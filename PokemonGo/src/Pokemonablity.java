
public class Pokemonablity {
	private int jinha;
	private static String attribute;
	
	Pokemonablity(int jinha, String attribute){
		this.setJinha(jinha);
		this.attribute =attribute;
	}

	public int getJinha() {
		return jinha;
	}

	public void setJinha(int jinha) {
		this.jinha = jinha;
	}

	public static String getAttribute() {
		return attribute;
	}

	
	
  
}
